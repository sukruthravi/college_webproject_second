                        <p class="paragraph">Causes of Cankers: This is a fungal disease that mostly attacks bark of woody plants, lower hidden areas, small areas of dead tissue and in cracks or wounds of the tree. The canker disease mainly caused by biotic fungi and bacteria or extreme temperature conditions. Improper panting, hail, high winds, root rot, insect borers may also cause the canker disease.

Types of Cankers: Cytospora Canker and Nectria Canker.

Symptoms of Cankers: The symptoms are circular, discolored areas on the bark of the trees.

                        </p>
                    <h3 class="heading-tertiaryu u-margin-bottom-small">Control tips:</h3>

                    <ul class="paragraph">
                        <li>Disease resistant cultivars should be used.</li>
<li>Cut off the affected branches or limbs with proper pruning methods.</li>
<li>Avoid any injury to roots and trunk as wounds are the main cause of spreading canker disease.</li>
<li>Control the weeds by taking appropriate intercultural operation.</li>
<li>Limiting pruning of wounds may also prevent this kind of disease.</li>
<li>Avoid cutting into trunk cankers as it may renew fungal activity. No effective chemical controls are available to prevent or control the fungi that cause this disease.</li>

