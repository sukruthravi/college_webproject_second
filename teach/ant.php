                        <p class="paragraph">Ants can spoil more than a picnic. Some species can destroy wood buildings, and others can bite. Although ants play important roles in nature, they don't belong in people's homes. Fortunately, there are many simple steps you can take to resolve the problem once you know what kind of ant you are dealing with.
                        </p>
                    <h3 class="heading-tertiaryu u-margin-bottom-small">Control tips:</h3>

                    <ul class="paragraph">
                        <li>Knowing what the ant needs is vital to making your home or yard less inviting. Some ants eat wood. Others eat people's food, and some feed on decaying plants and other insects. Check out the resources below or contact your Cooperative Extension Service for help identifying ants.</li>
<li>Seal any cracks and crevices that ants may use to enter your home.</li>
<li>Wipe up ant trails with soapy water to remove any pheromone trails.</li>
<li>Regularly empty and wash garbage cans and recycling bins. Clean up crumbs and spills, especially with sweet liquids like soda.</li>
<li>If ants are coming inside, consider keeping food in hard, sealed containers, not cardboard boxes or paper wrappers. Attractive items, such as sugar and honey, can also be stored in the refrigerator.</li>
<li>If a potted plant has ants, consider creating a 'moat' barrier around them. Elevate potted plants inside a larger bowl full of water and detergent keeping them above the water line.</li>

