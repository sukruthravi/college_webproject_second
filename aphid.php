                                                 Aphids
                                            </h2>
                        </div>
            

                
                <div class="row">
                    <div class="col-1-of-2">
                        
                        <p class="paragraph">Aphids damage a huge variety of host plants by sucking the juices from leaves and stems, causing discoloration, leaf curling, yellowing, and stunted growth.Honeydew can attract ants, and fuel the growth of fungus on plant surfaces. Aphids can also transmit plant viruses, injecting them into the plant as they feed. These viruses can cause molting, yellowing, or poor yields in various garden vegetables and ornamentals. In an aphid's average lifetime of one month, they can produce 40-85 offspring. Some aphids have wings; some do not.
                        </p>
                    <h3 class="heading-tertiaryu u-margin-bottom-small">Control tips:</h3>

                    <ul class="paragraph">
                        <li>An Integrated Pest Management (IPM) approach for aphids may involve biological, physical, and chemical controls.</li>
<li>Consider contacting your Cooperative Extension Service for assistance in identifying aphids. You might learn about simple control techniques and/or plant diseases that could have been transmitted.</li>
<li>Frequent monitoring helps in identifying early infestations. Regularly inspect new growth, flower buds, stems, and tender shoots.
</li>
<li>Be sure to check the underside of leaves. Monitor when temperatures make aphids more active, between 65° to 80°F.</li>
<li>Avoid overusing nitrogen fertilizer, which aphids love. Use only the amount recommended on the fertilizer label.</li>




                    </ul>
