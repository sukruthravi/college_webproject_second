                        <p class="paragraph">Causes of Rust Disease: Rusts are nothing but fungal diseases ((pathogenic fungi) that can cause colored powder on leaves. Generally, rusts require a living host to complete their life cycle.

Types of  Rust Diseases: Asparagus Rust, other type rusts include white pine blister rust, wheat rust, and cedar-apple rust.

Symptoms of Rust Disease: Rust disease is mainly seen in mid to late summer and autumn.

Rusts make pale leaf spots and they finally end up into spore-producing structures which are being called as “pustules”.
The pale leaf spots are found on the lower side of the leaf.
Rust disease stop the growth of the plant by killing the vigor.

